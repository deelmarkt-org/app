/// DeelMarkt icon size tokens.
///
/// Matches Phosphor Icons sizing defined in
/// `docs/design-system/components.md` → Icons section.
abstract final class DeelmarktIconSize {
  /// 16px — Inline with small text, badges.
  static const double xs = 16;

  /// 20px — Inside buttons.
  static const double sm = 20;

  /// 24px — Navigation bars, cards, list tiles.
  static const double md = 24;

  /// 32px — Empty states, section headers.
  static const double lg = 32;

  /// 48px — Onboarding shield/feature icons.
  static const double xl = 48;

  /// 64px — Hero illustrations (onboarding, splash).
  static const double hero = 64;
}
